    private void loadBooks(){
        try {
            dbBookDAO librosDB = new dbBookDAO();
            List<Book> listaLibros = librosDB.listar();
            DefaultTableModel model = (DefaultTableModel) TableBook.getModel();
            for(Book libro : listaLibros){
                model.addRow(new Object[]{libro.getId(), libro.getTitle()});
            }
        } catch (Exception e) {
            System.err.println(e.getMessage());
        }
    }

