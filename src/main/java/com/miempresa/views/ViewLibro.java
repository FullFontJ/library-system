    private void btnEditBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditBookActionPerformed
        if(TableBook.getSelectedRow() > -1){
            int indiceLibro = (int) TableBook.getValueAt(TableBook.getSelectedRow(), 0);
            Librarysystem.changePanel(new FormNuevoLibro(indiceLibro));
        }
    }//GEN-LAST:event_btnEditBookActionPerformed

    private void btnDeleteBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteBookActionPerformed
        dbBookDAO eliminarLibro = new dbBookDAO();
        DefaultTableModel model = (DefaultTableModel) TableBook.getModel();
        for (int i : TableBook.getSelectedRows()) {
            try {
                eliminarLibro.eliminar((int) TableBook.getValueAt(i, 0));
                model.removeRow(i);
            } catch (Exception e) {
                System.out.println("Error");
            }
        }
        JOptionPane.showMessageDialog(null, "Los libros selecionados an sido eliminados", "Libro", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnDeleteBookActionPerformed

