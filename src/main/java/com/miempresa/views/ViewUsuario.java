    private void LoadUsers(){
        try{
            dbUserDAO loadUsuarios = new dbUserDAO();
            List<User> lista = loadUsuarios.listar();
            DefaultTableModel model = (DefaultTableModel) TableUsers.getModel();
            for(User usuario : lista){
                model.addRow(new Object[]{usuario.getId(), usuario.getName()});
            }
//            loadUsuarios.listar().forEach((u) -> model.addRow(new Object[]{u.getId(), u.getName()}));
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }

