    private void btnDeleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteUserActionPerformed
        
        dbUserDAO deleteUsuario = new dbUserDAO();
        DefaultTableModel model = (DefaultTableModel) TableUsers.getModel();
        for(int i : TableUsers.getSelectedRows()){
            try{ 
                deleteUsuario.eliminar((int) TableUsers.getValueAt(i, 0));
                model.removeRow(i);
            }catch(Exception e){
                System.out.println("Error");
            }
        }
        
        JOptionPane.showMessageDialog(null, "La seleciÃ³n de usuarios an sido eliminados.", "Usuario", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnDeleteUserActionPerformed

